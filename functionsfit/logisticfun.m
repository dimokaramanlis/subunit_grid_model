function [f, J, H] = logisticfun(x)
%UNTITLED3 Summary of this function goes here
%   Detailed explanation goes here

%define inputs
x     = x(:);
%==========================================================================
f      = (1 +  exp(-x)).^-1;
%==========================================================================
if nargout>1
    J = f .* (1 - f);
end
%==========================================================================
if nargout>2
    H = J .* (1 - 2 * f);
end
%==========================================================================
end

